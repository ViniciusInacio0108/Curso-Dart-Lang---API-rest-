void main () {
  print("02.1) Operadores relacionais (== != > >= =< <)");

  /* Isso é super simples também. Básico do básico.
      ==    ->    igual a
      !=    ->    diferente de
      >     ->    maior que
      <     ->    menor que
      >=    ->    maior ou igual a
      <=    ->    menor ou igual a
   */

  var nota = 69;
  var resultado = nota >= 70;

  print("Aluno aprovado: ${resultado}");
}