void main() {
  print("04.2) Funções de Arrays");
  print("");

  var array = [1];
  array.add(2); // colocar o elemento ao final do array
  array.insert(1, 2); // colocar o elemento em um index específico nesse caso 1
  array.removeAt(1); // apaga no index selecionado no parâmentro

  /* Tem diversos outros super úteis e padrões que podem ser usados. 
  Não os colocarei aqui pois não vejo a necessidade de colocar tantos. */
}
